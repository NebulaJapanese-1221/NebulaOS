/// Draws the cursor at the specified location using the given style.
pub fn draw_cursor(fb: &mut Framebuffer, style: CursorStyle, x: isize, y: isize, clip: Rect) {
    let (width, height) = if let Some(ref info) = fb.info {
        (info.width as isize, info.height as isize)
    } else {
        return;
    };

    let (mut draw_x, mut draw_y) = (x, y);
    if style == CursorStyle::Crosshair {
        draw_x -= 6;
        draw_y -= 8;
    }

    let bitmap = match style {
        CursorStyle::Arrow => &CURSOR_ARROW,
        CursorStyle::ResizeNS => &CURSOR_RESIZE_NS,
        CursorStyle::Hand => &CURSOR_HAND,
        CursorStyle::IBeam => &CURSOR_IBEAM,
        CursorStyle::Crosshair => &CURSOR_CROSSHAIR,
        CursorStyle::ResizeEW => &CURSOR_RESIZE_EW,
        CursorStyle::ResizeNWSE => &CURSOR_RESIZE_NWSE,
        CursorStyle::ResizeNESW => &CURSOR_RESIZE_NESW,
        CursorStyle::Wait => &CURSOR_WAIT,
        CursorStyle::Help => &CURSOR_HELP,
    };

    for (dy, row) in bitmap.iter().enumerate() {
        for (dx, &pixel) in row.iter().enumerate() {
            if pixel == 0 { continue; }

            let color = if pixel == 1 { 0x00_00_00_00 } else { 0x00_FF_FF_FF };
            let px = draw_x + dx as isize;
            let py = draw_y + dy as isize;

            if clip.contains(px, py) && px >= 0 && py >= 0 && px < width && py < height {
                fb.set_pixel(px as usize, py as usize, color);
            }
        }
    }
}